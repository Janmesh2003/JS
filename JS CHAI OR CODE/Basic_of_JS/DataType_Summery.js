/*                     ## Primitive ## 


7 type ke Primitive DataType hai :- String , Number , Boolean , Null , undefined , Symbol ,
BigInt
                            
1. Number :- Represent both integer and floating-point 
   Number Example : 5 , 3.14.

2. String :- Represent sequence of charaters enclosed on single or double quotes .
   Example : "hello" , " Janmesh " .

3. Boolean :- Represent a binary value ,either true or false ,often used for conditional 
   logic.

4. Null :- Standalone value it is represenation of empty value it is special type and it is
   a object type. 
   Example : let y = null;
  
5. Undefined :-represents a variable that has been declared but has not been assigned a 
   value yet.
   Example : let x;

6. Symbol :- It is mostly used to find uniqnece.

7. BigInt :- Represents large integers that cannot be represented by the Number type.

*/ 

